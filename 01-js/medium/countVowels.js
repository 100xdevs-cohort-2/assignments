function countVowels(str) {
    // Your code here
    let ans = 0;
    for(let i = 0; i<str.length; i++){
      switch(str[i]){
        case 'a':
        case 'e':
        case 'i':
        case 'o':
        case 'u':
        case 'A':
        case 'E':
        case 'I':
        case 'O':
        case 'U': ans++;
      }
    }
    return ans;
}


